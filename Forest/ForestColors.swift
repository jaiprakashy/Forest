    }
    
    static var matureTreeColor: UIColor {
        return #colorLiteral(red: 0.3, green: 0.6, blue: 0.2,alpha: 1)
    }
    
    static var elderTreeColor: UIColor {
        return #colorLiteral(red: 0.2, green: 0.4, blue: 0.1,alpha: 1)
    }
    
    static var treeColor: UIColor {
        return #colorLiteral(red: 0.3, green: 0.6, blue: 0.2, alpha: 1)
    }
    
    static var bearColor: UIColor {
        return #colorLiteral(red: 0.4, green:0.2, blue: 0.0, alpha: 1)
    }
    
    static var woodcutterColor: UIColor {
        return #colorLiteral(red: 0.8, green: 0, blue: 0, alpha: 1)
    }
    
    static var groundColor: UIColor {
        return #colorLiteral(red: 0.9, green:0.9, blue: 0.8, alpha: 1)
    }
}
